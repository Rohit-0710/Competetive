package model;

public class CompareModel {
	
	private String start;
	private String test;
	
	
	public String getStart() {
		return start;
	}


	public void setStart(String start) {
		this.start = start;
	}


	public String getTest() {
		return test;
	}


	public void setTest(String test) {
		this.test = test;
	}


	public CompareModel(String start, String test) {
		super();
		this.start = start;
		this.test = test;
	}
	
	
	
	
	
}
